/**
 * 类型
 */
public class Types {
    public static void main(String[] args) {

        // ===== 基本数据类型 =====
        // 字节 byte   8bit
        byte bit = 100;

        // 短整型 short   16bit
        short shortNum = 10;

        // 整型 int   32bit
        int num = 11;

        // 长整型 long   64bit
        long longNum = 100l;

        // 单精度浮点型 float 32bit
        float price = 12.2f;

        // 双精度浮点型 double 64bit
        double totalPrice = 12.2222d;

        // 布尔值 boolean 1bit
        boolean isDelete = false;

        // 字符 char 16bit
        char c = 'c';

        // ===== 引用类型 =====

        // ===== 强制类型转换 =====
        int cNum = c; // 99
    }
}